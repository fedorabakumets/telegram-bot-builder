/**
 * @fileoverview Типы счётчика сообщений пользователя
 * @description Интерфейс для подсчёта сообщений, отправленных пользователем и ботом в диалоге
 * @module
 */

/**
 * Счётчик сообщений в диалоге пользователя с ботом
 * @interface UserMessageCounts
 * @description Используется для отображения статистики переписки в деталях пользователя
 * @description Подсчитывается на основе истории сообщений из базы данных
 */
export interface UserMessageCounts {
  /**
   * Количество сообщений, отправленных пользователем боту
   * @type {number}
   * @required
   * @description Включает текстовые сообщения, нажатия кнопок, отправку медиа
   */
  userSent: number;

  /**
   * Количество сообщений, отправленных ботом пользователю
   * @type {number}
   * @required
   * @description Включает приветствия, вопросы, ответы бота, медиа-сообщения
   */
  botSent: number;

  /**
   * Общее количество сообщений в диалоге
   * @type {number}
   * @required
   * @description Вычисляется как сумма userSent + botSent
   */
  total: number;
}
