/**
 * @fileoverview Типы счётчика сообщений пользователя
 * @description Интерфейс для подсчёта сообщений, отправленных пользователем и ботом
 */

/**
 * Счётчик сообщений пользователя
 * @interface
 */
export interface UserMessageCounts {
  /** Количество сообщений, отправленных пользователем */
  userSent: number;
  /** Количество сообщений, отправленных ботом */
  botSent: number;
  /** Общее количество сообщений */
  total: number;
}
