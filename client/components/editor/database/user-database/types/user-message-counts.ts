/**
 * Счётчик сообщений в диалоге пользователя с ботом
 */
export interface UserMessageCounts {
  /** Количество сообщений, отправленных пользователем боту */
  userSent: number;

  /** Количество сообщений, отправленных ботом пользователю */
  botSent: number;

  /** Общее количество сообщений в диалоге */
  total: number;
}
